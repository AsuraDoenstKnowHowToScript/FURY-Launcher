namespace Launcher.Core.Localization;

/// <summary>
/// Tiny in-memory localization. All UI text lives here as key -> string tables per
/// <see cref="Language"/>. Zero UI/Avalonia dependency: the App reads <see cref="T(string)"/>
/// and re-applies text when <see cref="Changed"/> fires. Missing keys fall back to English.
/// </summary>
public static class Loc
{
    public static Language Current { get; private set; } = Language.English;

    /// <summary>Raised after the language changes so the UI can re-apply all text.</summary>
    public static event Action? Changed;

    public static void SetLanguage(Language lang)
    {
        if (Current == lang) return;
        Current = lang;
        Changed?.Invoke();
    }

    /// <summary>Looks up a key in the current language, falling back to English, then the key.</summary>
    public static string T(string key)
    {
        if (_tables.TryGetValue(Current, out var table) && table.TryGetValue(key, out var s))
            return s;
        if (_tables[Language.English].TryGetValue(key, out var en))
            return en;
        return key;
    }

    /// <summary>Formats a localized template with arguments (e.g. "version {0}").</summary>
    public static string T(string key, params object?[] args) => string.Format(T(key), args);

